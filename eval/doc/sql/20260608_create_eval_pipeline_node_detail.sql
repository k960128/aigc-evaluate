CREATE TABLE eval_pipeline_node_detail (
    id BIGINT NOT NULL COMMENT '主键ID',
    task_id BIGINT NOT NULL COMMENT '评测任务ID，对应 eval_task.id',
    task_detail_id BIGINT NOT NULL COMMENT '任务执行批次ID，对应 eval_task_detail.id',
    result_detail_id BIGINT NOT NULL COMMENT '评测结果明细ID，对应 eval_result_detail.id',
    sample_id BIGINT NULL COMMENT '数据集样本ID，对应 dataset_sample.id',
    node_code VARCHAR(32) NOT NULL COMMENT '节点编码：MODEL_CALL-模型调用，L1-L1字面量拦截，L2-L2召回，L3-L3裁决',
    status VARCHAR(32) NOT NULL COMMENT '节点状态：RUNNING-执行中，PASSED-通过，BLOCKED-拦截，FAILED-失败，SKIPPED-跳过，STOPPED-终止',
    start_time DATETIME NULL COMMENT '节点开始时间',
    end_time DATETIME NULL COMMENT '节点结束时间',
    latency INT NULL COMMENT '节点耗时，单位毫秒',
    error_msg VARCHAR(1000) NULL COMMENT '节点错误信息或短路原因',
    input_snapshot JSON NULL COMMENT '节点输入快照，保存必要上下文',
    output_snapshot JSON NULL COMMENT '节点输出快照，保存模型输出、召回结果或裁决结果摘要',
    node_result JSON NULL COMMENT '节点结构化结果，例如 L1 命中的 riskDetailsId、keyword、warningTags、decision',
    create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    deleted TINYINT(1) NOT NULL DEFAULT 0 COMMENT '是否删除',
    PRIMARY KEY (id),
    KEY idx_eval_pipeline_node_task_detail_id (task_detail_id),
    KEY idx_eval_pipeline_node_result_detail_id (result_detail_id),
    KEY idx_eval_pipeline_node_result_node (result_detail_id, node_code),
    KEY idx_eval_pipeline_node_task_node_status (task_detail_id, node_code, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='评测流水线节点执行明细表';
